public class OperadoresAritmeticos {

    void main(){

        //Soma
        System.out.println(10 + 2);

        //subtração
        System.out.println(8-3);

        //multiplicação
        System.out.println(8*2);

        //divisao
        System.out.println(10/2);

        //resto
        System.out.println(13%2);
    }
    
}
