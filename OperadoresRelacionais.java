public class OperadoresRelacionais {
    
    void main(){
        
        //Variaveis
        int num1 = 10;
        int num2 = 5;

        //Operadores Relacionais

        //igual
        System.out.println(num1 == num2);
        //diferente
        System.out.println(num1 != num2);
        //maior >
        System.out.println(num1 > num2);
        //maior >=
        System.out.println(num1>= num2);
        //menor <
        System.out.println(num1< num2);
        //menor <=
        System.out.println(num1<= num2);


    }
}
